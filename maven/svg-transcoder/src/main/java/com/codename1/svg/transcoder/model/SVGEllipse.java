package com.codename1.svg.transcoder.model;

public final class SVGEllipse extends SVGShape {
    private float cx, cy, rx, ry;

    public float getCx() { return cx; }
    public void setCx(float cx) { this.cx = cx; }
    public float getCy() { return cy; }
    public void setCy(float cy) { this.cy = cy; }
    public float getRx() { return rx; }
    public void setRx(float rx) { this.rx = rx; }
    public float getRy() { return ry; }
    public void setRy(float ry) { this.ry = ry; }
}
