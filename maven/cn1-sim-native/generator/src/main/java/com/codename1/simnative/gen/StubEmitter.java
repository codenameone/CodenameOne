package com.codename1.simnative.gen;

import java.io.File;
import java.io.FileOutputStream;
import java.io.OutputStreamWriter;
import java.io.Writer;
import java.lang.reflect.Method;
import java.lang.reflect.Modifier;
import java.util.ArrayList;
import java.util.Collections;
import java.util.Comparator;
import java.util.List;

/**
 * Generates weak no-op definitions for every ParparVM-convention symbol the
 * JNI shims reference. The simulator build compiles only a subset of the
 * port's native sources; any symbol not provided by a compiled source file is
 * satisfied by these stubs at link time, and because they are weak
 * (__attribute__((weak))) a real definition always wins.
 *
 * <p>Stub behavior: log once per symbol (so a missing feature is visible in
 * the console rather than silently dead) and return zero/NULL.</p>
 *
 * <p>Usage: StubEmitter &lt;nativeClassName&gt; &lt;outputDir&gt; &lt;suffix&gt;
 * [definedSymbolsFile]</p>
 */
public class StubEmitter {
    public static void main(String[] args) throws Exception {
        if (args.length < 3 || args.length > 4) {
            System.err.println("Usage: StubEmitter <nativeClassName> <outputDir> <suffix> [definedSymbolsFile]");
            System.exit(1);
        }
        Class cls = Class.forName(args[0]);
        File outDir = new File(args[1]);
        outDir.mkdirs();
        String suffix = args[2];

        List<Method> methods = new ArrayList<Method>();
        for (Method m : cls.getDeclaredMethods()) {
            if (Modifier.isNative(m.getModifiers())) {
                methods.add(m);
            }
        }
        Collections.sort(methods, new Comparator<Method>() {
            public int compare(Method a, Method b) {
                int n = a.getName().compareTo(b.getName());
                if (n != 0) {
                    return n;
                }
                return ParparMangler.jniMethodDescriptor(a).compareTo(ParparMangler.jniMethodDescriptor(b));
            }
        });

        java.util.Set<String> defined = null;
        if (args.length == 4) {
            defined = new java.util.HashSet<String>();
            java.io.BufferedReader r = new java.io.BufferedReader(
                    new java.io.InputStreamReader(new java.io.FileInputStream(args[3]), "UTF-8"));
            try {
                String line;
                while ((line = r.readLine()) != null) {
                    line = line.trim();
                    if (!line.isEmpty()) {
                        defined.add(line);
                    }
                }
            } finally {
                r.close();
            }
        }

        StringBuilder b = new StringBuilder();
        b.append("// GENERATED FILE - DO NOT EDIT\n");
        b.append("// Generated by com.codename1.simnative.gen.StubEmitter for ").append(cls.getName()).append('\n');
        b.append("// Weak no-op definitions: a strong definition in any compiled\n");
        b.append("// native source file always overrides the stub.\n");
        b.append("#include <stdio.h>\n");
        b.append("#include \"cn1_globals.h\"\n\n");
        b.append("#define CN1SIM_STUB_LOG(name) do { \\\n");
        b.append("    static int warned = 0; \\\n");
        b.append("    if (!warned) { warned = 1; fprintf(stderr, \"cn1sim: stubbed native called: %s\\n\", name); } \\\n");
        b.append("} while (0)\n\n");

        for (Method m : methods) {
            String sym = resolvedSymbol(m, defined);
            String ret = ParparMangler.parparType(m.getReturnType());
            boolean isVoid = m.getReturnType() == Void.TYPE;
            b.append("__attribute__((weak)) ").append(isVoid ? "void" : ret).append(' ')
                    .append(sym).append('(').append(params(m)).append(") {\n");
            b.append("    CN1SIM_STUB_LOG(\"").append(sym).append("\");\n");
            if (!isVoid) {
                if ("JAVA_OBJECT".equals(ret)) {
                    b.append("    return JAVA_NULL;\n");
                } else {
                    b.append("    return 0;\n");
                }
            }
            b.append("}\n\n");
        }
        write(new File(outDir, "cn1sim_stubs_" + suffix + ".c"), b.toString());
        System.out.println("Generated " + methods.size() + " weak stubs for " + cls.getName());
    }

    private static String resolvedSymbol(Method m, java.util.Set<String> defined) {
        StringBuilder full = new StringBuilder();
        full.append(ParparMangler.mangleClassName(m.getDeclaringClass()))
                .append('_').append(m.getName().replace('-', '_')).append("__");
        StringBuilder plain = new StringBuilder(full);
        for (Class p : m.getParameterTypes()) {
            String ext = ParparMangler.typeExtension(p);
            full.append(ext);
            plain.append(ext);
        }
        if (m.getReturnType() != Void.TYPE) {
            full.append("_R").append(ParparMangler.typeExtension(m.getReturnType()));
        }
        if (defined != null && !defined.contains(full.toString()) && defined.contains(plain.toString())) {
            return plain.toString();
        }
        return full.toString();
    }

    private static String params(Method m) {
        StringBuilder b = new StringBuilder("CODENAME_ONE_THREAD_STATE");
        if (!Modifier.isStatic(m.getModifiers())) {
            b.append(", JAVA_OBJECT instanceObject");
        }
        int i = 0;
        for (Class p : m.getParameterTypes()) {
            b.append(", ").append(ParparMangler.parparType(p)).append(" arg").append(i++);
        }
        return b.toString();
    }

    private static void write(File f, String content) throws Exception {
        Writer w = new OutputStreamWriter(new FileOutputStream(f), "UTF-8");
        try {
            w.write(content);
        } finally {
            w.close();
        }
    }
}
