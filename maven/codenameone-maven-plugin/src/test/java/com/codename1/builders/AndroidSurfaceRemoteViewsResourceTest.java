package com.codename1.builders;

import java.io.BufferedReader;
import java.io.IOException;
import java.io.InputStream;
import java.io.InputStreamReader;
import java.nio.charset.StandardCharsets;
import org.junit.jupiter.api.Test;

import static org.junit.jupiter.api.Assertions.assertFalse;
import static org.junit.jupiter.api.Assertions.assertNotNull;
import static org.junit.jupiter.api.Assertions.assertTrue;

class AndroidSurfaceRemoteViewsResourceTest {

    @Test
    void spacerUsesARemoteViewsInflatableRoot() throws IOException {
        String path = "/com/codename1/builders/surfaces/android/layout/cn1_surface_spacer.xml";
        InputStream stream = getClass().getResourceAsStream(path);
        assertNotNull(stream, "missing generated Android surface spacer layout");

        StringBuilder xml = new StringBuilder();
        try (BufferedReader reader = new BufferedReader(
                new InputStreamReader(stream, StandardCharsets.UTF_8))) {
            String line;
            while ((line = reader.readLine()) != null) {
                xml.append(line).append('\n');
            }
        }

        assertTrue(xml.toString().contains("<FrameLayout"),
                "FrameLayout is permitted in RemoteViews on current Android releases");
        assertFalse(xml.toString().contains("<View "),
                "android.view.View is rejected by RemoteViews inflation on API 36");
    }
}
