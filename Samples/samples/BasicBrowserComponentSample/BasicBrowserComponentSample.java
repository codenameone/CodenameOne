public class BasicBrowserComponentSample {

    private Form current;
    private Resources theme;

    public void init(Object context) {
        // use two network threads instead of one
        updateNetworkThreadCount(2);

        theme = UIManager.initFirstTheme("/theme");

        // Enable Toolbar on all Forms by default
        Toolbar.setGlobalToolbar(true);

        // Pro only feature
        Log.bindCrashProtection(true);

        addNetworkErrorListener(err -> {
            // prevent the event from propagating
            err.consume();
            if(err.getError() != null) {
                Log.e(err.getError());
            }
            Log.sendLogAsync();
            Dialog.show("Connection Error", "There was a networking error in the connection to " + err.getConnectionRequest().getUrl(), "OK", null);
        });
        Display.getInstance().setProperty("BrowserComponent.useCEF", "true");
    }
    
    public void start() {
        if(current != null){
            current.show();
            return;
        }
        Form hi = new Form("Hi World", new BorderLayout());
        hi.getToolbar().addMaterialCommandToRightBar("Popup", FontImage.MATERIAL_OPEN_IN_NEW, ev->{
            Sheet sheet = new Sheet(null, "A Popop");
            sheet.add(BorderLayout.CENTER, new Label("Hello World"));
            sheet.show();
        });
        BrowserComponent cmp = new BrowserComponent();
        
        cmp.setURL("https://www.codenameone.com");
        hi.add(BorderLayout.CENTER, cmp);
        
        Button hello = new Button("Hello");
        hello.addActionListener(evt->{
            cmp.execute("if (confirm('continue?')) alert('testing');");
        });
        
        Button prompt = new Button("Prompt");
        prompt.addActionListener(evt->{
            cmp.execute("var name = prompt('What is your name'); if (name) alert('Hello ' + name);");
        });
        hi.add(BorderLayout.SOUTH, GridLayout.encloseIn(2, hello, prompt));
        hi.show();
    }

    public void stop() {
        current = getCurrentForm();
        if(current instanceof Dialog) {
            ((Dialog)current).dispose();
            current = getCurrentForm();
        }
    }
    
    public void destroy() {
    }

}
